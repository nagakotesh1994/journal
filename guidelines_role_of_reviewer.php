				<h5>INSTRUCTIONS TO REVIEWER</h5><hr>
				<p align="justify">
					<b>Criteria for Reviewing:</b> Reviewers could judge any manuscript based on following criteria:<br><br>

					<b>i) Format of the Article:</b> Any major divergence from the standard manuscript format should be indicated.<br><br>
					<b>ii) Technical Presentation:</b> The research article should be technically presented instead of being presented as a story. Mere repetition of past work should not be accepted. You can look for conceptual advancement over previously published work. Any major omission of the previously published findings on the similar problem must be checked.<br><br>
					<b>iii) Interpretation of Result:</b> The discussion should hover around the result and should not include irrelevant and unachievable statement.<br><br>
					<b>iv) Statistical Presentation:</b> Proper statistical tools should be applied over the data wherever found necessary.<br><br>
					<b>v) Plagiarism of Data:</b> Data showing any type of suspicion, duplication and manipulation must be brought to the notice of the author(s).<br><br>
					<b>vi) Summary:</b> Highlight the strength and weakness of the article considering potential importance of the work in the context of present and future.<br><br>
					<b>vii) Conclusion:</b> at the end, reviewer(s) can recommend necessary corrections needed to accept the article / research paper, if they are actually required, else recommend it for publication. If the article / research paper found unsuitable that should be declared as unacceptable for publication.
