				published works. Reviewers should not consider manuscripts with conflicts of interest. If a
				potential reviewer feels unqualified to review the manuscript, that reviewer should notify the
				editors immediately and decline the review. Manuscripts received for review will be treated as
				confidential documents and shall not be shown or discussed with other without prior
				authorization from the editors. Authors should expect to receive reviewer reports in a prompt
