				<h5>DOUBLE-BLIND PEER REVIEW PROCESS</h5>
				<p align="justify">Both the reviewer and the author are anonymous in this model. Author anonymity limits reviewer
				bias, for example based on an author&#39;s gender, country of origin, academic status or previous publication
				history. Articles written by prestigious or renowned authors are considered based on the content of their
				papers, rather than their reputation.</p>
				<p align="justify">Reviewers&#39; comments to the editors are confidential and before passing on to the author will be
