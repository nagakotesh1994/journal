					<li><b>Figures:</b><br>
						<p align="justify">All photographs, graphs and diagrams should be referred to as a ‘Figure’ and they should be numbered consecutively (i, ii etc).<br>
						Figures may be sized to fit within the column(s) of the journal.<br>
						Provide a detailed legend (without abbreviations) to each figure; refer to the figure in the endnote its approximate location.</p>
					</li>

