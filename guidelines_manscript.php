				<h5>ROLE OF AUTHORS</h5>
				<p align="justify">Authors of original research (not previously published or under consideration for publication
				elsewhere) should ensure about accurate presentation of the work carried out, a discussion of the
				significance of the work in context with previous works, and should contain sufficient experimental detail
				to allow others to replicate the work. Appropriate citation of previously published works must always be
				included. Authors should disclose any financial or other conflicts of interest that may be construed as
				influencing the data or interpretation. All sources of financial support should be disclosed. Authorship
				should be limited to those persons who have made a significant contribution to the work in terms of
				conception, design, experimental implementation, and data analysis and interpretation. All persons
				making significant contributions should be included as co-authors. If an author discovers a significant
				error in the published work, the author is obligated to inform the journal editor in order to either correct or
				retract the paper.</p>
				<h5>MANUSCRIPT PRESENTATION</h5>
				<ol>
