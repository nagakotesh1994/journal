				reports. Whether significant revisions are proposed, acceptance is dependent on whether the author can
				deal with those satisfactorily.</p>

				<h5>PUBLICATION POLICY</h5>
				<p align="justify">The journal employs the double-blind peer review process, where both reviewers and
				authors remain anonymous throughout the review process.</p>
				<p align="justify">Every Manuscript submitted for publication reviewed by the editor. If the paper complies with the editorial policies and with a minimum quality level, then it is sent to two reviewers
				(Double-blind peer review).</p>

