					<li><p><b>Font, space, margins:</b></p>
						<ul type="disc">
							<li>A4 paper size (Portrait)</li>
							<li>Double-space all material, including notes and references.</li>
							<li>Times New Roman</li>
							<li>Text 12-point (Foot Notes 10 points)</li>
							<li>Double-spaced lines of text</li>
							<li>24 or 25 lines of text</li>
							<li>1.5 inch margins</li>
							<li>Paragraph indentation of 0.5 inches</li>
						</ul>
					</li>
					<li><p><b>First page should contain:</b></p>
						<ul type="disc">
							<li>Running head</li>
							<li>Article type</li>
