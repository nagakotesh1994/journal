							<li>Affiliations(s)</li>
							<li>Full address for correspondence, including telephone, fax number and e-mail address</li>
						</ul>
					</li>

					<li><p><b>Abstract</b></p>
						<ul type="disc">
							<li>Please provide an abstract of 100-125 words. The abstract should not contain any undefined abbreviations of unspecified references.</li>
							<li>5 keywords must be included.</li>
						</ul>
					</li>

